// write a program to print hello world 5 times
public class p_01_helloWorld {

  public static void main(String[] args) {
    int i; // counter variable
    for (i = 0; i < 5; i++) {
      System.out.println("Hello");
    }
    System.out.println();
    for (i = 5; i > 0; i--) {
      System.out.println("World");
    }
  }
}
